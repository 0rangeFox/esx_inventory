    `description` TEXT DEFAULT NULL,
    `weight` INT NOT NULL DEFAULT 0,
    `closeOnUse` TINYINT NOT NULL DEFAULT 0,
    `max` INT NOT NULL DEFAULT 100,

    PRIMARY KEY (`id`)
);

