CREATE TABLE IF NOT EXISTS `ammos` (
    `id` INT NOT NULL AUTO_INCREMENT,
    `owner` TEXT NOT NULL,
    `hash` TEXT NOT NULL,
    `count` INT NOT NULL DEFAULT 0,

    PRIMARY KEY (`id`)
);

CREATE TABLE IF NOT EXISTS `inventories` (
    `id` INT NOT NULL AUTO_INCREMENT,
    `owner` TEXT NOT NULL,
    `type` TEXT DEFAULT NULL,
    `data` LONGTEXT NOT NULL,

    PRIMARY KEY (`id`)
);

